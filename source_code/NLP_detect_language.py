import textblob

from textblob import TextBlob  # Εισαγωγή της βιβλιοθίκης Textblob

blob = TextBlob('Ποιο είναι το όνομά σου;')  # Καταχωρούμε το κείμενο που θέλουμε για εντοπισμό.

lang = blob.detect_language()  # Χρησιμοποιείται αυτή η μέθοδος για τον εντοπισμό της γλώσσας

print(lang)  # Εδώ τυπώνετε το αποτέλεσμα της μέθοδού, το οποίο είναι ο εντοπισμός της γλώσσας.